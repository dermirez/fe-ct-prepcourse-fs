function esPositivoOInferiorA10(a) {
   // La función recibe un número "a" por parámetro.
   // Retorna true si es positivo y menor que 10.
   // Retorna false en caso contrario.
   // Tu código:

   // 1. a > 0
   // 2. a < 10
   // 3. 1 && 2

   return a >= 0 && a <= 10;
}

module.exports = esPositivoOInferiorA10;
